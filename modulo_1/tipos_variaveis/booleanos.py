# Operadores Lógicos: and e or

# AND - se duas delas forem verdadeiras
if True and True:
        print("Bloco deverá ser executado")
if True and False:
        print("Bloco não deverá ser executado")
if False and False:
        print("Bloco não deverá ser executado")
        
# OR - se uma delas for verdadeiras
if True or False:
        print("Bloco OR deverá ser executado")
if False or False:
        print("Bloco OR não deverá ser executado")
if True or True:
        print("Bloco OR não deverá ser executado")