# Inteiro
numero_inteiro = 3
print("Inteiro = ", numero_inteiro)
# Real com ponto flutuante
numero_real = 3.14
print("numero_real= ", numero_real)

#type()
print("Tipo da variável inteiro =", type(numero_inteiro)) #int
print("Tipo da variável real =", type(numero_real)) #float

# soma +
soma = 1 + 1
print("1 + 1 = ", soma)

# subtração -
subtracao = 2 - 1
print("2 - 1 = ", subtracao)

# multiplicação *
multiplicacao = 2 * 3
print("2 * 3 = ", multiplicacao)

# divisão /
divisao = 5 / 2
print("5 / 2 = ", divisao)
print("Tipo da variavel do resultado da divisão = ", type(divisao))

# int() -> conversao de float p/ int
print("Valor em inteiro = ", int(divisao))

# float() -> conversao de int p/ float
print("Valor em float = ", float(divisao))

# exponenciação **
exponenciacao = 2 ** 3
print("2 ** 3 = ", exponenciacao)

# resto da divisão % - Módulo
resto_divisao = 5 % 2
print("5 % 2 = ", resto_divisao)

# divisão inteiro //
divisao = 5 // 2
print("5 // 2 = ", divisao)
print("Tipo da variavel do resultado da divisão = ", type(divisao))